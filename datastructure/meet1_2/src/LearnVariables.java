public class LearnVariables {
    public static void main(String[] args) throws Exception{
        //variable declaration (assign data type to int)
        int bulat;
        //variable definition (assign a value to the variable according to its data type)
        bulat = 2024;
        //variable declaration and definition
        double phi = 3.14;
        float desimal = 1.333f;
        char huruf = 'a';
        char uniChar = '\u0061';
        char emoji = '\u263A';
        boolean status = true;

        System.out.println("Nilai dari variable bulat = " + bulat);
        System.out.println("Nilai dari variable phi = " + phi);
        System.out.println("Nilai dari variable desimal = " + desimal);
        System.out.println(huruf);
        System.out.println(uniChar);
        System.out.println(emoji);
        System.out.println(status);

    }
}
